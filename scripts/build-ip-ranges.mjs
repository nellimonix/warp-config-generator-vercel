#!/usr/bin/env node
/**
 * Auto-generate IP_RANGES blocks in worker/api-handler.js and functions/api/generate.js
 * from config/services/*.json.
 *
 * Each JSON: { name, icon, iconLibrary, type?, ips }  — only `ips` is used here.
 *
 * Block boundaries in the target files are marked with:
 *   // IP_RANGES:BEGIN
 *   const IP_RANGES = { ... }
 *   // IP_RANGES:END
 *
 * Idempotent: re-running with no service changes produces no diff.
 */

import { readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const SERVICES_DIR = join(ROOT, 'config', 'services');

const TARGETS = [
  join(ROOT, 'worker', 'api-handler.js'),
  join(ROOT, 'functions', 'api', 'generate.js'),
];

const BEGIN = '// IP_RANGES:BEGIN';
const END = '// IP_RANGES:END';
const HEADER = '// IP_RANGES:BEGIN (auto-generated by scripts/build-ip-ranges.mjs — do not edit by hand)';

function loadRanges() {
  const files = readdirSync(SERVICES_DIR)
    .filter((f) => f.endsWith('.json'))
    .sort();

  const ranges = {};
  for (const file of files) {
    const key = file.replace(/\.json$/, '');
    const data = JSON.parse(readFileSync(join(SERVICES_DIR, file), 'utf8'));
    if (typeof data.ips === 'string' && data.ips.length > 0) {
      ranges[key] = data.ips;
    }
  }
  return ranges;
}

function renderBlock(ranges) {
  const lines = [HEADER, 'const IP_RANGES = {'];
  for (const [key, ips] of Object.entries(ranges)) {
    lines.push(`  ${key}: ${JSON.stringify(ips)},`);
  }
  lines.push('}');
  lines.push(END);
  return lines.join('\n');
}

function replaceBlock(filePath, block) {
  const src = readFileSync(filePath, 'utf8');
  const startIdx = src.indexOf(BEGIN);
  const endIdx = src.indexOf(END);

  if (startIdx === -1 || endIdx === -1 || endIdx < startIdx) {
    throw new Error(`Markers not found in ${filePath}. Expected ${BEGIN} ... ${END}`);
  }

  const before = src.slice(0, startIdx);
  const after = src.slice(endIdx + END.length);
  const next = before + block + after;

  if (next === src) {
    return false;
  }
  writeFileSync(filePath, next);
  return true;
}

const ranges = loadRanges();
const block = renderBlock(ranges);

let anyChanged = false;
for (const t of TARGETS) {
  const changed = replaceBlock(t, block);
  console.log(`${changed ? 'updated' : 'unchanged'}: ${t}`);
  anyChanged = anyChanged || changed;
}

console.log(`\n${Object.keys(ranges).length} services processed.`);
process.exit(0);
